public class Formacion {
    public int porteros;
    public int defensas;
    public int medios;
    public int delanteros;

    public Formacion(int porteros, int defensas, int medios, int delanteros) {
        this.porteros = porteros;
        this.defensas = defensas;
        this.medios = medios;
        this.delanteros = delanteros;
    }
}
