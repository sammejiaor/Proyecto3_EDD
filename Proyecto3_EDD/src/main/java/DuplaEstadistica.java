public class DuplaEstadistica {
    public int origen;
    public int destino;
    public int peso;
    public int asistencias;
    public int pasesClave;

    public DuplaEstadistica(int origen, int destino, int peso, int asistencias, int pasesClave) {
        this.origen = origen;
        this.destino = destino;
        this.peso = peso;
        this.asistencias = asistencias;
        this.pasesClave = pasesClave;
    }
}
